function per (){
	var uno = document.getElementById('pippo').value
	var due = document.getElementById('pluto').value
	var risultato = uno * due ;
	document.getElementById('moltiplicazione').innerHTML = risultato;
}



function div (){
	var uno = document.getElementById('pippo').value
	var due = document.getElementById('pluto').value
	var risultato = uno / due ;
	document.getElementById('divisione').innerHTML = risultato;
}
